export const FIELD_FONT_FAMILIES = PORTFOLIO_FONTS;

export function findFontByValue(value) {
  if (!value) return PORTFOLIO_FONTS[0];
  return PORTFOLIO_FONTS.find((item) => item.value === value) || { id: 'custom', label: 'Custom', value, category: 'system' };
}

export function findFontById(id) {
  return PORTFOLIO_FONTS.find((item) => item.id === id) || null;
}

export function getFontsByCategory(categoryId) {
  return PORTFOLIO_FONTS.filter((font) => font.category === categoryId);
}

export function getTemplateBodyFont(content) {
  return content?.templateBodyFont || content?.templateFont || '';
}

export function getTemplateHeadingFont(content) {
  return content?.templateHeadingFont || '';
}

export function collectFontValuesFromContent(content) {
  const values = new Set();
  const bodyFont = getTemplateBodyFont(content);
  const headingFont = getTemplateHeadingFont(content);
  if (bodyFont) values.add(bodyFont);
  if (headingFont) values.add(headingFont);
  if (content?.fieldStyles) {
    Object.values(content.fieldStyles).forEach((style) => {
      if (style?.fontFamily) values.add(style.fontFamily);
    });
  }
  return [...values];
}
